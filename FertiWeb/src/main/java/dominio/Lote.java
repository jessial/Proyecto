package dominio;

public class Lote {

	private Usuario usuario;
	private int codigo;
	private String ubicacion;
	private int area;
	
	public int getCodigo() {
		return codigo;
	}
	public String getUbicacion() {
		return ubicacion;
	}
	public int getArea() {
		return area;
	}
	public Usuario getUsuario() {
		return usuario;
	}
}
